#ШАГ 1. СОЗДАЕМ СПИСОК КУБОВ НЕЧЕТНЫХ ЧИСЕЛ ОТ 1 ДО 1000
cubes = []
a=1
while a<1000:
    x = a**3
    cubes.append(x)
    a = a+2
#print('set of cubes is:', cubes)

#ШАГ "+17": добавляем 17 к каждому элементу списка "cubes"
n=0
p=0
new17 = []
for p in cubes:
    p = cubes[n]
    p = p+17
    n = n+1
    new17.append(p)
#print(new17)


