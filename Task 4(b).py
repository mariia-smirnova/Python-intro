# ВНЕШНИЙ ЦИКЛ (FOR) - ПЕРЕБОР ВСЕХ ЧИСЕЛ ИЗ СПИСКА КУБОВ
# ВНУТРЕННИЙ ЦИКЛ (WHILE) - СУММИРОВАНИЕ ЦИФР В ЧИСЛЕ
